package com.javabasic.www;

public class RelationalOperator {
	
	public static void main(String [] args) {

	int num1 = 10;
	int num2 = 20;
	
	if(num1 == num2) {
		
		System.out.println("Number is equal");
	}
		else {
			
			System.out.println("Number is not equal");
		}
	
	if(num1 != num2) {
		System.out.println("Number not equal");
	}
	else {
		
		System.out.println("number is equal");
	}
	
	if(num1 > num2) {
		
		System.out.println("num 1 is greater than num2");
		
	}
	
	else {
		
		System.out.println("Num1 is not greater");
	}
	
	if(num1 >= num2) {
		
		System.out.println(" num1 is greater and equal to  num2");
	}
	
	else {
		
		System.out.println("num1 not greater and equal");
	}
	
	if(num1 < num2) {
		
		System.out.println("num1 less");
		
		
	}
	
	else {
		
		System.out.println("num1 not less");
	}
	
	if(num1 <= num2) {
		
		System.out.println("num1 is less and equal");
	}
	
	else {
		
		System.out.println("num1 is not less and equal");
	}
	}
	
	
}
