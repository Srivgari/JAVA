package com.javabasic.www;

public class AutoOperatorDemo {
	
	public static void main(String [] args) {
		 
		int num1 = 100;
		int num2 = 200;
		
		num1++;
		num2--;
		 System.out.println("number is :" +num1);
		 System.out.println("number is: " +num2);
		 
		num2= num1+num2;
		
		++num2;
		
		System.out.println("number is: " +num2);
		
	}
	

}
